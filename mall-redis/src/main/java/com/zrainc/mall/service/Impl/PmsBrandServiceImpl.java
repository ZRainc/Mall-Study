package com.zrainc.mall.service.Impl;

import com.github.pagehelper.PageHelper;
import com.zrainc.mall.mbg.mapper.PmsBrandMapper;
import com.zrainc.mall.mbg.model.PmsBrand;
import com.zrainc.mall.mbg.model.PmsBrandExample;
import com.zrainc.mall.service.PmsBrandService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;

